library(ggplot2)
library(dplyr)
library(tidyr)
library(grid)
library(png)

df <- read.csv("datos/mr.csv", na.strings = "")
str(df)

drops  <- c("Eleccion.2018", "Eleccion.2021")
df  <- df[, !(names(df) %in% drops)]

#Revisar transformación de los datos, no logré eliminar las
#columnas que incluían el porcentaje de cambios entre legislaturas

df1<- df %>%
  gather("Legislatura","MayoriaRelativa",-Partido)

img <- png::readPNG("img/logo.png")
g_pic  <- rasterGrob(img, interpolate = TRUE)

fig  <- ggplot(df1, aes( x = reorder(Partido, -MayoriaRelativa), MayoriaRelativa, fill=Legislatura)) + geom_col(position = "dodge2")
fig + theme_bw() + labs(title="Distribución de la Mayoría Relativa por partidos", subtitle ="Legislaturas LXIII,LXIV y LXV", caption = "Fuente: SIL") +
  xlab("Partidos") + ylab("Cantidad de diputados") + theme(plot.title = element_text(hjust = 0.5), plot.subtitle = element_text(hjust = 0.5)) +  annotation_custom(g_pic, xmin=5, xmax=Inf, ymin=5, ymax=Inf)
